from src.util.json_util import ConfigSingleton
import json
from cryptography.fernet import Fernet
import pyodbc
import os


class HeliosDB:
    _instance = None  # Statická proměnná pro uchování instance

    def __new__(cls):
        """Singleton pattern"""
        if cls._instance is None:
            cls._instance = super(HeliosDB, cls).__new__(cls)            
        return cls._instance
     
    def __init__(self):
        self.config = ConfigSingleton()
        self._app_login =  self.config.get('helios_app_login')
        self._ip_address = self.config.get('helios_ip_address')
        self._db_catalog = self.config.get('helios_db_catalog')
        self._workstation = os.environ.get("COMPUTERNAME", "Unknown")
        self.connection = None
        self._connected = False

    def get_password(self):
        # Tvůj šifrovací klíč (musí být stejný jako při šifrování)
        with open("crypto.key", "rb") as key_file:
            key = key_file.read()
       
        cipher = Fernet(key)     
        # Dešifrování hesla
        return cipher.decrypt( self.config.get('helios_app_password').encode()).decode()


    @property
    def connected(self):
        return self._connected

    def __del__(self):
        try:
            if self.connection and self._connected:
                self.connection.close()
        except Exception as ex:
            print(f"Error closing connection in Helios destructor: {ex}")

    def get_connection(self):
        return self.connection

    def close(self):
        try:
            if self.connection and self._connected:
                self.connection.close()
            self._connected = False
        except Exception as ex:
            print(f"Error closing connection: {ex}")


    def send_query(self, sql_query, values=None):
        try:
            cursor = self.connection.cursor()
            if (values == None):
                cursor.execute(sql_query)
            else:
                cursor.execute(sql_query, values) 
            #rows = cursor.fetchall()

            columns = [column[0] for column in cursor.description]

            # Získání první řádky dat
            row = cursor.fetchone()

            # Sloučení názvů sloupců s daty
            #result = dict(zip(columns, row))


            print(columns)  # Výstup: {'column1': value1, 'column2': value2, ...}
            print(row)
            cursor.close()

            return row 

        except Exception as e:
            print("Error connection to database: ", e)


    def get_table_defs(self, table_name):
        cursor = self.connection.cursor()
        cursor.execute("""
            SELECT COLUMN_NAME, DATA_TYPE, CHARACTER_MAXIMUM_LENGTH, IS_NULLABLE, COLUMN_DEFAULT
            FROM INFORMATION_SCHEMA.COLUMNS
            WHERE TABLE_NAME = ?
        """, (table_name,))

        columns = cursor.fetchall()
        if not columns:
            print(f"Tabulka {table_name} neexistuje.")
            exit()

        column_defs = []
        for col in columns:
            col_def = f"{col[0]} {col[1]}"

            # Přidání maximální délky pro typy, které ji podporují (např. varchar)
            if col[1] in ['varchar', 'char', 'nvarchar', 'nchar'] and col[2]:
                if col[2] > 0:  # Ošetření nulové délky
                    col_def += f"({col[2]})"

            # Kontrola, zda je sloupec NOT NULL
            if col[3] == 'NO':
                col_def += " NOT NULL"

            # Kontrola pro DEFAULT hodnotu, která není None nebo 0
            if col[4] and col[4] != '0':  # Ošetření default hodnoty '0' nebo None
                col_def += f" DEFAULT {col[4]}"

            column_defs.append(col_def)

        return ", ".join(column_defs)


            
        
    def connect(self):
        connection_string = (
            f'DRIVER={{SQL Server}};'
            f'SERVER={self._ip_address}\\SQLHELIOS;'
            f'DATABASE={self._db_catalog};'
            f'UID={self._app_login};'
            f'PWD={self.get_password()};'
            f'Workstation ID={self._workstation};'
        )
        try:
            self.connection = pyodbc.connect(connection_string)
            self._connected = True
            print(f"Connected\n")
            return True
        
        except Exception as ex:
            print(f"Function Helios::Connect error: {ex}")
            if self.connection:
                self.connection.close()
            return False
        

